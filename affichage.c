#include <stdio.h>
#include <stdlib.h>
#include "lecture.h"
#include "affichage.h"

void afficheLabyrinthe(char labyTab[TAILLE_X][TAILLE_Y]){
	for (int i = 0; i < TAILLE_Y; i++){
        for (int j = 0; j < TAILLE_X; j++){
        	printf("%c", labyTab[j][i]);
        }
    }

    printf("\n");
}